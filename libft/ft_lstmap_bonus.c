#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*n_list;
	t_list	*n_node;
	t_list	*temp;

	n_list = NULL;
	n_node = NULL;
	temp = lst;
	while (temp)
	{
		n_node = ft_lstnew(f(temp->content));
		if (!n_node)
		{
			ft_lstclear(&n_list, del);
			return (NULL);
		}
		ft_lstadd_back(&n_list, n_node);
		temp = temp->next;
	}
	return (n_list);
}
